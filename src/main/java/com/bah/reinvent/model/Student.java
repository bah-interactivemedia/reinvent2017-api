package com.bah.reinvent.model;

import java.io.Serializable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "student")
public class Student implements Serializable {


    private String student_id;
    private String student_first;
    private String student_last;
    private String student_token;
    private String student_dob;
    private String student_email;
    private String student_gender;


    public Student() {
    }

    @DynamoDBHashKey(attributeName = "student_id")
    @DynamoDBAutoGeneratedKey
    public String getStudent_id() {
        return student_id;
    }
    @DynamoDBAttribute
    public String getStudent_first() {
        return student_first;
    }
    @DynamoDBAttribute
    public String getStudent_last() {
        return student_last;
    }
    @DynamoDBAttribute
    public String getStudent_token() {
        return student_token;
    }
    @DynamoDBAttribute
    public String getStudent_dob() {
        return student_dob;
    }
    @DynamoDBAttribute
    public String getStudent_email() {
        return student_email;
    }
    @DynamoDBAttribute
    public String getStudent_gender() {
        return student_gender;
    }


    public void setStudent_id(String student_id) {
        this.student_id = student_id;
    }

    public void setStudent_first(String student_first) {
        this.student_first = student_first;
    }

    public void setStudent_last(String student_last) {
        this.student_last = student_last;
    }

    public void setStudent_token(String student_token) {
        this.student_token = student_token;
    }

    public void setStudent_dob(String student_dob) {
        this.student_dob = student_dob;
    }

    public void setStudent_email(String student_email) {
        this.student_email = student_email;
    }

    public void setStudent_gender(String student_gender) {
        this.student_gender = student_gender;
    }
}